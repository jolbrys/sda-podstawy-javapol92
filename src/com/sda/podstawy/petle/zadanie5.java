package com.sda.podstawy.petle;

public class zadanie5 {
    //stworz petle wewnatrz petli. (pamietaj o roznych nazwach iteratorow)
    //wyswietl wartosc iteratorow w postaci x:y

    public static void main(String[] args) {
        for(int i = 0; i < 10; i++){
            for(int j = 0; j < 10; j++){
                System.out.print(i + ":" + j + " ");
            }
            System.out.print("\n");
        }
    }




}
